#include <stdio.h>

int main(void)
{
    printf("alert: \a\n");
    printf("backspace: \b\n");
    printf("formfeed: \f\n");
    printf("newline: \n");
    printf("carriage return: \r\n");
    printf("horizontal tab: \t\n");
    printf("vertical tab: \v\n");
    printf("backslash: \\\n");
    printf("single quote: \'\n");
    printf("double quote: \"\n");
    printf("question mark: \?\n");
    printf("octal (lambda): \316\273\n");
    printf("hexadecimal (lambda): \xCE\xBB\n");
    return 0;
}